function openNav(){


    document.getElementById('myMainMenu').style.width="800px"
    document.getElementById('myMainMenu').style.zIndex="2"
    document.getElementById('myMainMenu').style.transition="0.5s"
    document.getElementById('myMainMenu').style.boxShadow="offset-x"
   
   
   


}

function closeNav(){


    document.getElementById('myMainMenu').style.width="0"
    document.getElementById('myMainMenu').style.zIndex="0"
    document.getElementById('myMainMenu').style.transition="0.5s"
    
}